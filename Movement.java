public static void move(String key, String[][]Current, int y_position, int x_position)
	{
		int x = 0;
		int y = 0;
		if (key.equals("w"))
		{
			y = -1;
			x = 0;
		}
		if (key.equals("s"))
		{
			y = 1;
			x = 0;
		}
		if (key.equals("d"))
		{
			y = 0;
			x = 1;
		}
		if (key.equals("a"))
		{
			y = 0;
			x = -1;
		}
		
		String hold = Current[y_position][x_position];
		Current[y_position][x_position] = Current[y_position+y][x_position+x];
		Current[y_position+y][x_position+x] = hold;
	}

